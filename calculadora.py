number_1 = int(input('Digite o primeiro número: '))
number_2 = int(input('Digite o segundo número: '))


print('{} + {} = '.format(number_1, number_2))
print(number_1 + number_2)

#Subtração

print('{} - {} = '.format(number_1, number_2))
print(number_1 - number_2)

#Multiplicação

print('{} * {} = '.format(number_1, number_2))
print(number_1 * number_2)

#Divisão

print('{} / {} = '.format(number_1, number_2))
print(number_1 / number_2)